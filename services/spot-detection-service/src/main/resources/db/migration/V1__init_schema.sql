CREATE EXTENSION IF NOT EXISTS postgis;

CREATE TABLE spots (
    id           UUID          PRIMARY KEY,
    lot_id       UUID          NOT NULL,
    spot_number  VARCHAR(10)   NOT NULL,
    floor        INT           NOT NULL DEFAULT 1,
    state        VARCHAR(20)   NOT NULL DEFAULT 'FREE',
    last_updated TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    latitude     DOUBLE PRECISION NOT NULL,
    longitude    DOUBLE PRECISION NOT NULL,
    location     GEOGRAPHY(POINT, 4326)
);

CREATE TABLE spot_events (
    id         UUID        PRIMARY KEY DEFAULT gen_random_uuid(),
    spot_id    UUID        NOT NULL REFERENCES spots(id),
    state      VARCHAR(20) NOT NULL,
    source     VARCHAR(50) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_spots_lot_id       ON spots(lot_id);
CREATE INDEX idx_spots_state        ON spots(state);
CREATE INDEX idx_spots_location     ON spots USING GIST(location);
CREATE INDEX idx_spot_events_spot   ON spot_events(spot_id);
CREATE INDEX idx_spot_events_time   ON spot_events(created_at DESC);

